function updateRandomStats(score) {
    // 1. Yerel Kayıt
    let data = getLocalRandomStats();
    data.allScores.push(score);
    localStorage.setItem('kelimelik_random_history', JSON.stringify(data));

    // 2. Firebase Aylık Kayıt
    const userId = getMyStatsId();
    const date = new Date();
    const monthKey = `stats_${date.getFullYear()}_${date.getMonth() + 1}`;
    
    const userRef = db.collection('users').doc(userId);
    
    db.runTransaction(async (t) => {
        const doc = await t.get(userRef);
        let uData = doc.exists ? doc.data() : {};
        
        let mData = uData[monthKey] || { count: 0, totalScore: 0, avg: 0, maxScore: 0 };
        
        mData.count++;
        mData.totalScore += score;
        mData.avg = Math.round(mData.totalScore / mData.count);
        if (score > mData.maxScore) mData.maxScore = score; // Rekor Takibi

        // Global Rekor (Ömür boyu)
        let gData = uData.global || { maxScore: 0 };
        if (score > gData.maxScore) gData.maxScore = score;

        t.set(userRef, { 
            [monthKey]: mData,
            global: gData,
            lastUpdated: firebase.firestore.FieldValue.serverTimestamp()
        }, { merge: true });
    });
}

function fetchLeaderboard() {
    const tbody = document.getElementById('leaderboardBody');
    if (!tbody) return;
    tbody.innerHTML = '<tr><td colspan="3">Yükleniyor...</td></tr>';

    const date = new Date();
    const monthKey = `stats_${date.getFullYear()}_${date.getMonth() + 1}`;

    db.collection('users')
        .orderBy(`${monthKey}.avg`, 'desc')
        .limit(10)
        .get()
        .then(snap => {
            let html = '';
            let rank = 1;
            snap.forEach(doc => {
                const d = doc.data()[monthKey];
                if (d && d.count >= 10) { // En az 10 oyun kuralı
                    const isMe = (doc.id === getMyStatsId());
                    html += `<tr class="${isMe ? 'highlight' : ''}">
                        <td>${rank++}</td>
                        <td>Oyuncu-${doc.id.substr(0,4)}</td>
                        <td>${d.avg}</td>
                    </tr>`;
                }
            });
            tbody.innerHTML = html || '<tr><td colspan="3">Veri yok.</td></tr>';
        });
}




