// HÜCRE TIKLAMA (TEK VE ÇOK KİŞİLİK + ÇİFT TIKLAMA ONAYLI)
// ==========================================

async function handleCellClick(index) {
    // 1. Kontrol: Yerleştirme modunda mıyız?
    if (!placementMode) {
        statusMsg.textContent = "HATA: Şu anda yerleştirme yapamazsınız.";
        return;
    }

    // --- YENİ: İKİ AŞAMALI ONAY MEKANİZMASI ---
    
    // Durum 1: İlk defa tıklanıyor veya farklı bir hücreye tıklandı
    if (selectedDraftIndex !== index) {
        selectedDraftIndex = index; // Yeni hücreyi seç
        
        // Grid'i tekrar çiz ki sarı renk görünsün
        // myGridData global değişkenini kullanıyoruz (Firebase'den en son gelen veri)
        renderGrid(myGridData, 'myGrid'); 
        
        return; // Fonksiyondan çık, veritabanına yazma!
    }
    
    // Durum 2: Zaten seçili olan (Sarı) hücreye tekrar tıklandı -> İŞLEMİ YAP!
    // Seçimi sıfırla
    selectedDraftIndex = null;

    // Gerekli Global Değişken Kontrolleri
    // myGridData'nın listenToGame içinde güncellendiğinden emin olun.
    if (myGridData[index] !== '') {
        statusMsg.textContent = "Bu hücre zaten dolu.";
        return;
    }

    // 25. Hamle yerel kontrolü (myFinalLetter)
    if (myPlayerId === 'PlayerA' && window.myFinalLetterA) { /*OK*/ } 
    
    // Transaction Başlatılıyor
    const gameRef = db.collection('games').doc(currentGameId);

    try {
        await db.runTransaction(async (transaction) => {
            const doc = await transaction.get(gameRef);
            if (!doc.exists) throw new Error("Oyun bulunamadı.");
            
            const data = doc.data();
            const currentMoveNumber = data.moveNumber;
            const isSinglePlayer = data.isSinglePlayer || false; // Tek kişilik mi?
            
            // --- HARF BELİRLEME ---
            let letterToPlace;
            const isFinalMove = (currentMoveNumber === 25);

            if (isFinalMove) {
                if (!myFinalLetter) throw new Error("25. Hamle için harf seçilmedi.");
                letterToPlace = myFinalLetter;
            } else {
                if (!data.currentLetter) throw new Error("Harf verisi yok.");
                letterToPlace = data.currentLetter;
            }
            
            // --- GRID GÜNCELLEME ---
            let myCurrentGrid = (myPlayerId === 'PlayerA') ? [...data.gridA] : [...data.gridB];
            
            if (myCurrentGrid[index] !== '') throw new Error("Dolu hücre.");
            myCurrentGrid[index] = letterToPlace; 

            let updatePayload = {};
            if (myPlayerId === 'PlayerA') updatePayload.gridA = myCurrentGrid;
            else updatePayload.gridB = myCurrentGrid;

            // --- TUR ATLAMA MANTIĞI ---
            
            // SENARYO 1: TEK KİŞİLİK OYUN
            if (isSinglePlayer) {
                if (isFinalMove) {
                    // Oyun Bitti
                    updatePayload.status = 'finished';
                    updatePayload.currentLetter = null;
                } else {
                    // Sonraki tura geç
                    const nextMove = currentMoveNumber + 1;
                    updatePayload.moveNumber = nextMove;
                    
                    // 25. Hamleye geçiş mi?
                    if (nextMove === 25) {
                         updatePayload.currentLetter = null; // Manuel seçim için sıfırla
                    } else {
                         // Diziden sıradaki harfi al
                         updatePayload.currentLetter = data.letterSequence[currentMoveNumber]; 
                    }
                }
            } 
            // SENARYO 2: ÇOK OYUNCULU (Klasik Mantık)
            else {
                let oppCurrentGrid = (myPlayerId === 'PlayerA') ? data.gridB : data.gridA;
                const oppFilledCount = oppCurrentGrid.filter(c => c !== '').length;

                // Rakip de oynamışsa tur atla
                if (oppFilledCount === currentMoveNumber) {
                    if (currentMoveNumber >= 25) {
                         updatePayload.status = 'finished';
                    } else if (currentMoveNumber === 24) {
                         updatePayload.moveNumber = firebase.firestore.FieldValue.increment(1);
                         updatePayload.currentLetter = null; 
                    } else {
                         updatePayload.moveNumber = firebase.firestore.FieldValue.increment(1);
                         // Random mod ise harfi güncelle
                         if (data.gameMode === 'RANDOM') {
                             updatePayload.currentLetter = data.letterSequence[currentMoveNumber];
                         } else {
                             updatePayload.currentLetter = null;
                         }
                         updatePayload.turnOwner = (data.turnOwner === 'PlayerA') ? 'PlayerB' : 'PlayerA';
                    }
                }
            }
            
            transaction.update(gameRef, updatePayload);
        });
        
        if (placementMode && myFinalLetter) myFinalLetter = null; 

    } catch (e) {
        console.error("Hücre tıklama hatası:", e);
        statusMsg.textContent = "Hata: " + e.message;
        // Hata durumunda seçimi sıfırlamak iyi olabilir
        selectedDraftIndex = null;
        renderGrid(myGridData, 'myGrid');
    }
}

// ==========================================
// PUAN HESAPLAMA (HIYERARŞİK VE KOMBİNASYONLU)
// ==========================================

function calculateScore(gridData) {
    const GRID_SIZE = 5;
    let totalScore = 0;
    let foundWords = new Set(); // Puanlanan kelimeleri listelemek için

    // Puan Kuralları
    const SCORE_RULES = { 2: 2, 3: 3, 4: 6, 5: 10 };

    /**
     * Bir segment (örn: "TATOK" veya "OALEL") içindeki en yüksek puanı hesaplar.
     * Kurallar:
     * 1. 5 veya 4 harfli kelime varsa, SADECE o puanı al ve bitir.
     * 2. Yoksa, çakışmayan 3 ve 2 harfli kelimelerin en yüksek toplamını bul.
     */
    const getSegmentMaxScore = (text) => {
        // --- ADIM 1: BASKIN KELİME KONTROLÜ (4 ve 5 Harfliler) ---
        
        // 5 Harfli Kontrolü
        if (text.length === 5 && isValidWord(text)) {
            foundWords.add(text);
            return SCORE_RULES[5]; // 10 Puan, başka şeye bakma.
        }

        // 4 Harfli Kontrolü (0-3 veya 1-4 aralığı)
        // Eğer 4 harfli kelime bulunursa, kalan 1 harf kelime olamayacağı için direkt döneriz.
        for (let i = 0; i <= text.length - 4; i++) {
            const sub = text.substring(i, i + 4);
            if (isValidWord(sub)) {
                foundWords.add(sub);
                return SCORE_RULES[4]; // 6 Puan, başka şeye bakma.
            }
        }

        // --- ADIM 2: KOMBİNASYON KONTROLÜ (3 ve 2 Harfliler) ---
        // Eğer buraya geldiysek, 4 veya 5 harfli kelime YOKTUR.
        // Şimdi çakışmayan (overlap olmayan) en iyi 3 ve 2'lileri bulacağız.
        
        let maxComboScore = 0;
        let bestWords = []; // En iyi senaryodaki kelimeleri tutmak için

        // Olası tüm geçerli alt kelimeleri bul: {word: "TAT", start: 0, end: 3, score: 3}
        let validSubWords = [];
        for (let len = 3; len >= 2; len--) { // Önce 3'lüler, sonra 2'liler
            for (let i = 0; i <= text.length - len; i++) {
                const sub = text.substring(i, i + len);
                if (isValidWord(sub)) {
                    validSubWords.push({
                        word: sub,
                        start: i,
                        end: i + len,
                        score: SCORE_RULES[len]
                    });
                }
            }
        }

        // Çakışmayan kelimelerden maksimum puanı hesapla.
        // Grid boyutu 5 olduğu için olasılıklar sınırlıdır, basit bir iterasyon yeterli.
        
        // Senaryo A: Tek Kelimeler (Sadece bir tane 3'lü veya 2'li varsa)
        validSubWords.forEach(item => {
            if (item.score > maxComboScore) {
                maxComboScore = item.score;
                bestWords = [item.word];
            }
        });

        // Senaryo B: İki Kelime (Çakışmayan)
        // Birinci kelimeyi al, ikincisiyle çakışıyor mu kontrol et.
        for (let i = 0; i < validSubWords.length; i++) {
            for (let j = i + 1; j < validSubWords.length; j++) {
                const w1 = validSubWords[i];
                const w2 = validSubWords[j];

                // Çakışma Kontrolü: w1 bitişi w2 başlangıcından küçük veya eşit olmalı (veya tam tersi)
                // Ancak döngümüz sıralı gitmeyebilir, garantiye alalım:
                const isOverlapping = (w1.start < w2.end && w2.start < w1.end);

                if (!isOverlapping) {
                    const currentTotal = w1.score + w2.score;
                    if (currentTotal > maxComboScore) {
                        maxComboScore = currentTotal;
                        bestWords = [w1.word, w2.word];
                    }
                }
            }
        }

        // En iyi senaryodaki kelimeleri sonuç listesine ekle
        bestWords.forEach(w => foundWords.add(w));
        
        return maxComboScore;
    }

    // Yardımıcı fonksiyon: Grid verisinden satır/sütun stringi oluşturur
    const getLineString = (indices) => {
        // null/undefined olanları boşluk yap
        return indices.map(index => gridData[index] || ' ').join('');
    };

    // --- ANA DÖNGÜ: SATIR VE SÜTUNLARI TARA ---

    // 1. Yatay (Satır) Tarama
    for (let row = 0; row < GRID_SIZE; row++) {
        const indices = Array.from({ length: GRID_SIZE }, (_, i) => row * GRID_SIZE + i);
        const lineStr = getLineString(indices);
        
        // Satırı boşluklardan ayırarak segmentleri bul (örn: "A B" -> ["A", "B"])
        // "OALEL" -> ["OALEL"]
        const segments = lineStr.replace(/\s+/g, ' ').split(' ');
        
        segments.forEach(segment => {
            if (segment.length >= 2) { // En az 2 harf varsa puanlamaya değer
                totalScore += getSegmentMaxScore(segment);
            }
        });
    }

    // 2. Dikey (Sütun) Tarama
    for (let col = 0; col < GRID_SIZE; col++) {
        const indices = Array.from({ length: GRID_SIZE }, (_, i) => i * GRID_SIZE + col);
        const lineStr = getLineString(indices);
        
        const segments = lineStr.replace(/\s+/g, ' ').split(' ');
        
        segments.forEach(segment => {
            if (segment.length >= 2) {
                totalScore += getSegmentMaxScore(segment);
            }
        });
    }

   // Sonuçları döndür (SIRALAMA MANTIĞI GÜNCELLENDİ)
    return {
        score: totalScore,
        // Kelimeleri Set'ten Diziye çevir ve sırala
        words: Array.from(foundWords).sort((a, b) => {
            // 1. Kriter: Uzunluk (Büyükten küçüğe)
            if (b.length !== a.length) {
                return b.length - a.length;
            }
            // 2. Kriter: Eğer uzunluk aynıysa Alfabetik (A-Z)
            return a.localeCompare(b, 'tr');
        })
    };
}

