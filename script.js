async function createNewGame() {
    const code = Math.random().toString(36).substring(2, 6).toUpperCase();
    myPlayerId = 'PlayerA';
    currentGameId = code;
    
    // Mod Seçimi
    const gameModeSelect = document.getElementById('gameModeSelect');
    const selectedMode = gameModeSelect ? gameModeSelect.value : 'CLASSIC'; 
    
    let sequence = null;
    let initialLetter = null;

    // Bilgi Mesajı
    const statusEl = document.getElementById('lobbyStatus');
    if (statusEl) statusEl.textContent = "Oyun kuruluyor...";

    // Random Mod ise Harf Dizisi Üret
    if (selectedMode === 'RANDOM') {
        try {
            sequence = generateGameSequence();
            initialLetter = sequence[0];
        } catch (e) {
            console.error("Harf üretim hatası:", e);
            if (statusEl) statusEl.textContent = "Hata: Harf üretilemedi.";
            return;
        }
    }
    
    try {
        // Firestore'a Yeni Oyun Kaydı
        await db.collection('games').doc(code).set({
            status: 'waiting',      
            turnOwner: 'PlayerA',   
            moveNumber: 1,  
            isSinglePlayer: false,
            gameMode: selectedMode, 
            letterSequence: sequence,     
            currentLetter: initialLetter, 
            
            // --- TIMER VE SENKRONİZASYON ALANLARI ---
            // Oyun başladığında herkesin saati buradan hesaplanacak
            turnStartTime: firebase.firestore.FieldValue.serverTimestamp(),
            playerA_status: "waiting", // Henüz hamle yapmadı
            playerB_status: "waiting", 
            
            gridA: Array(25).fill(''),
            gridB: Array(25).fill(''),
            createdAt: firebase.firestore.FieldValue.serverTimestamp()
        });

        setupGameUI(code);
        
        // Multiplayer olduğu için rakip alanını açıyoruz
        const oppSection = document.getElementById('opponentSection');
        if (oppSection) oppSection.style.display = 'block';

        listenToGame(); // Dinlemeyi başlat (PART 4'te eklenecek)

    } catch (error) {
        console.error("Oyun Kurma Hatası:", error);
        if (statusEl) statusEl.textContent = "Bağlantı hatası.";
    }
}

